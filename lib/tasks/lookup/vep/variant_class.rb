module VariantClass
  def vc_to_id(str)
    case str
      when 'SNV'
        'SO:0001483'
      when 'genetic_marker'
        'SO:0001645'
      when 'substitution'
        'SO:1000002'
      when 'tandem_repeat'
        'SO:0000705'
      when 'Alu_insertion'
        'SO:0002063'
      when 'complex_structural_alteration'
        'SO:0001784'
      when 'complex_substitution'
        'SO:1000005'
      when 'copy_number_gain'
        'SO:0001742'
      when 'copy_number_loss'
        'SO:0001743'
      when 'copy_number_variation'
        'SO:0001019'
      when 'duplication'
        'SO:1000035'
      when 'interchromosomal_breakpoint'
        'SO:0001873'
      when 'interchromosomal_translocation'
        'SO:0002060'
      when 'intrachromosomal_breakpoint'
        'SO:0001874'
      when 'intrachromosomal_translocation'
        'SO:0002061'
      when 'inversion'
        'SO:1000036'
      when 'loss_of_heterozygosity'
        'SO:0001786'
      when 'mobile_element_deletion'
        'SO:0002066'
      when 'mobile_element_insertion'
        'SO:0001837'
      when 'novel_sequence_insertion'
        'SO:0001838'
      when 'short_tandem_repeat_variation'
        'SO:0002096'
      when 'tandem_duplication'
        'SO:1000173'
      when 'translocation'
        'SO:0000199'
      when 'deletion'
        'SO:0000159'
      when 'indel'
        'SO:1000032'
      when 'insertion'
        'SO:0000667'
      when 'sequence_alteration'
        'SO:0001059'
      when 'probe'
        'SO:0000051'
      else
        raise ParseError("Unsupported consequence type: #{str}")
    end
  end

  class ParseError < StandardError; end
end
